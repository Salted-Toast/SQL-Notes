/*---------------Datatypes---------------*\

These datatypes are set within the table configuration
and apply for the entire row

-- Datatypes --

INT(length)         Integer, whole number
CHAR(length)        Character, if len=8 and input it hello stored as hello000
VARCHAR(length)     VariableCharacter, if len=50 and input hello stored as hello
DATE                Date, Values are stored as YYYY-MM-DD
(There are more just cba)

