-- Settings under table config --

PK  -   Makes this column contain the primary key
NN  -   Not Nullable, this makes the column unable to be null
B   -   Tells MySQL if its a binary column 
UN  -   Unsigned Datatype, tick this if the column is misc with no datatype
ZF  -   Fills all columns with 0 if its numeric data
AI  -   Auto Incriment, MySQL will incriment the previous value by one when another entry is made
G   -   Generated Column,

Default - Just whatever it will be set if nothing is put in its place


